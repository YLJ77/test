var effect = {
	change:function(obj,json,type,fn){
		var complete = false;
		var cur = 0;
		var speed = 0;
		var attr = null;
		var arr = [];
		obj.current_time +=1;
		for(var property in json){
			arr.push(property);
		}
		for(attr in json){
			var beginning_value = parseInt(parseFloat(attribute.get(obj,attr)));
			var change_in_value = json[attr] - beginning_value;
			console.log('obj.current_time: '+obj.current_time);
			console.log('beginning_value: '+beginning_value);
			console.log('change_in_value: '+change_in_value);
			console.log("type['duration']: "+type['duration']);
			var value = Tween[type['tween_type']][type['ease_type']](obj.current_time,beginning_value,change_in_value,type['duration'])
			attribute.set(obj,attr,value);
			if(json[attr] <= value){
				complete = true;
				break;
			}
		}
		if(complete){
			effect.stop(obj);
			if(fn) fn();
		}
	},
	stop:function(obj){
		clearInterval(obj.timer);
	},
	animate:function(obj,json,type,fn){
		clearInterval(obj.timer);
		obj.current_time = 0;
		obj.timer = setInterval(function(){
			effect.change(obj,json,type,fn);
		},30);
	}
}